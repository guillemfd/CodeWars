// /*
//  * Function to check whether the given number is Even or Odd.
//  * @params = integer number
//  * @return = returns "Even" if the number is even "Odd" otherwise.
//  */


function even_or_odd(number) {
    if (number%2 == 0) {
      return "Even";
    } else {
      return "Odd";
    }
  }